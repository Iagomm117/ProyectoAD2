    public void setIconImagenLabel(String url) throws IOException{
        File imagen = new File(url);
        Image image = ImageIO.read(imagen);
        Image imagenRedimensionada = image.getScaledInstance(75, 75, Image.SCALE_SMOOTH);
        ImagenLabel.setIcon(new ImageIcon(imagenRedimensionada));
    }
    
